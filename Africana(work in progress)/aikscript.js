
const a_c = () =>{
    const dim = document.getElementsByClassName("dim1");
    const food = document.getElementById("wow1");
    const image = document.getElementById("tm1");
    const text = document.getElementById("a_c");
    const content = document.getElementById("content1");
    const tribe = document.getElementById("tribe1");
    const sidedish = document.getElementById("side1");
    const ing = document.getElementById("ingredients1");
    const change = document.getElementsByClassName("change1");
    for(let i = 0; i != dim.length; i++){
            console.log(dim[i]);
            dim[i].style.opacity = "0.25";
    }
    for(let i = 0; i != change.length; i++){
        console.log(change[i]);
        change[i].style.display = "none";
    }
    food.style.display = "block";
    food.style.width = "100%";
    food.style.border = "3px solid teal";
    food.style.backgroundColor = "White";
    image.style.float = "right";
    image.style.padding = "10px"
    image.style.paddingBottom = "20px"
    image.style.width ="200px";
    image.style.height ="auto";
    text.style.color = "black";
    content.style.display = "flex";
    content.style.flexDirection = "column";
}
